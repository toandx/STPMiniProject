package com.example.to_do_list;

public class image_2 {
    int image ;
    String ten ;
    int ha;

    public image_2(int image, String ten, int ha) {
        this.image = image;
        this.ten = ten;
        this.ha = ha;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public int getHa() {
        return ha;
    }

    public void setHa(int ha) {
        this.ha = ha;
    }
}
